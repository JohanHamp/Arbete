        Tack för att du använder Pluggkollen.se (hädanefter refereras den till som sajten). Sajten är utvecklad av Grupp15 på Uppsala Universitet (vilka refereras till som Grupp15, vi eller oss) som också är ansvarig för att dina personuppgifter behandlas i enlighet med gällande lagstiftning.
        </br> </br>Om du har frågor kring hur dina personuppgifter behandlas kan du alltid kontakta oss via e-post på kontakt@pluggkollen.se. Vill du ha professionell studievägledning finns det ofta professionella studievägledare på kursernas institutioner att vända sig till. </br>
        </br>
        <p class="r1"> 1.0 Allmänt </p>
        Pluggkollen startades 2018 med syftet att sänka tröskeln för att söka kontakt för frågor inom utbildning och de kurser som erbjuds vid olika högskolor och universitet i landet. Tjänsten är kopplad till Antagning.se och man använder sitt vanliga konto där även för att logga in på Pluggkollen. Meningen med tjänsten är att du innan du söker nya kurser enkelt skall kunna fråga personer (kallade mentorer) som redan läst dessa kurser om i princip vad som helst - även om du frågor utanför kurser såsom studentliv. Du kan även själv vara mentor i dina avslutade kurser (som är maximalt 3 terminer gamla). Mentorer och sökande tar själva ansvar för innehållet i konversationerna. Vi ber endast att du tar hänsyn till de sociala regler som anges nedan.</br>
        </br>
        Du bör veta att Grupp15 och Pluggkollen.se är helt opartisk, opolitisk och finansieras av lärosäten i Sverige samt studierelaterade annonser.</br>
        </br>
        <p class="r1"> 1.1 Medlemskap och användarkonto</p>
        </br>
        Eftersom ditt medlemskap från antagning.se används hänvisar vi också till de användarvillkor du godkänt under din registrering där - de gäller även här. Du behöver inte ange någon ny information om dig själv om du inte vill. Informationen från antagning.se som används av sajten är ditt namn, ditt personnummer, din kontaktinformation och dina kurser. Vi kommer eventuellt att använda oss av dina kontaktuppgifter för att kontakta dig. Endast ditt namn kan initialt synas för andra användare. Du kommer däremot att ha möjligheten att fylla i mer information om dig själv om du vill. Den information om dig som du själv lägger till i din profil kommer sedan endast synas för de användare som du har kontakt med. Pluggkollen och Grupp15 sprider inte någon information om dig utanför sajten.</br>
        </br>
        <p class="r1"> 1.2 Ansvar vid användande av sajten</p>
        </br>
        Genom att använda tjänsten förbinder du dig att tala sanning inte använda kränkande uttryck.</br>
        </br>
        - Du får inte länka till olagligt innehåll på sajten.</br>
        - Du får inte använda material på sajten i andra sammanhang.</br>
        - Du får inte ta bort, dölja eller ändra något innehåll, juridiskt eller i sajtens kod, utanför de områden där redigering uttryckligen tillåts.</br>
        - Du får inte använda några varumärken eller logotyper på sajten utan tillåtelse från Grupp15.</br>
        - Grupp15 ansvarar endast för innehåll som vi lagt upp, inte det som individer lagt upp själva.</br>
        - Grupp15 ansvarar inte för kvaliteten av information given av mentorer.</br>
        </br>
        <p class="r1"> 1.3 Mentor</p>
        </br>
        Mentorer har ett större ansvar än de som inte väljer att vara mentorer eller inte kan vara mentorer. Som mentor förväntas det att du talar sanning och inte försöker vilseleda de personer du hjälper. Mentorer får inte dela med sig utav information om andra användare eller deras konversationer.</br>
        </br>
        <p class="r1"> 1.4 Hantering av personuppgifter</p>
        </br>
        För att tjänsten skall kunna fungera krävs att vi har tillgång till ditt konto från Antagning.se, där vi använder ditt namn, ditt personnummer, din mailadress och din studieinformation. Vi skyddar dessa uppgifter och delar inte dessa med andra användare eller personer utanför sajten. Vi använder dina personuppgifter för att:</br>
        </br>
        - Kunna matcha dig med mentorer.</br>
        - Identifiera din profil genom ditt namn för andra användare.</br>
        - Göra det möjligt att kommunicera genom meddelanden med andra användare.</br>
        - Kunna kommunicera med dig över e-post.</br>
        </br>
        Uppgifter du lämnar in till oss behandlas i enlighet med dataskyddsförordningen som 25/5-2018 ersätter personuppgiftslagen, PUL. Vill du inte att vi skickar mail till dig eller vill du hämta vilka uppgifter vi har om dig kan du kontakta oss på kontakt@pluggkollen.se. Genom att logga in på sajten med ditt användarkonto på antagning.se ger du Grupp15 samtycke att lagra dina personuppgifter med syftet att kunna utföra sajtens funktioner.
